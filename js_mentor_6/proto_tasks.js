//! Условия задачи:
// Реализуйте класс "Worker" (Работник), который будет иметь следующие свойства:
        // - name (имя);
        // - surname (фамилия);
        // - rate (ставка за день работы);
        // - days (количество отработаных дней).
// Кроме того, в реализуемом классе должен быть метод "getSalary()", который будет выводит в консоль значение величины зарлаты "работника". Рассчет зарплаты должен производится путем умножения ставки "rate" на количество отработаных работником дней - "days". Еще, в реализуемом классе должен быть метод "getFullName()", который должен выводить в консоль имя и отчество работника (путем соединения значений свойств "name" и "surname").

//! Решение задачи:
class Worker {
    constructor(name, surname, rate, days) {
        this.name = name;
        this.surname = surname;
        this.rate = rate;
        this.days = days;
    }
    getSalary() {
        return this.rate * this.days;
    }
    get getFullName() {
        return `${this.surname} ${this.name}`;
    }
};

//! Проверочные результаты решения задачи:
// Создаем новый проверочный объек на основе класса "Worker" и передаем ему набор исходных значении.
const worker = new Worker('Иван', 'Иванов', 10, 31);

// Выводим в лог созданный проверочный объект "worker":
console.log(worker);
console.log('-----------------------------------------------');

// Выводим в лог проверочные результаты:
console.log(worker.name); // Должен вывести => 'Иван'
console.log(worker.surname); // Должен вывести => 'Иванов'
console.log(worker.getFullName); // Должен вывести => 'Иванов Иван'
console.log(worker.rate); // Должен вывести => 10
console.log(worker.days); // Должен вывести => 31
console.log(worker.getSalary()); // Должен вывести => 310
console.log('***********************************************');


