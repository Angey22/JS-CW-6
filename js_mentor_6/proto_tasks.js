//! Условия задачи:
// Напишите новый класс "Boss", который должен наследоваться от класса "Worker" из предыдущего задания (из Задачи №1). В классе "Boss" должно появится новое свойство "workers" - содержащее в себе численное значение количества работников. Кроме того, зарплата в этом новом классе должна рассчитываться по другому принципу и вычислятся с помощью умножения значений свойст: rate (ставки руководителя) * days (количество отработаных дней) * workers (количество работников).

//! Решение задачи:
class Boss extends Worker {
    // Пишем "конструктор" со всеми переменными включая новую
    constructor(name, surname, rate, days, workers) {
        // В "" перечисляем все переменные наследуемого класса для того, что бы заново не повторять их в теле нового конструктора.
        super(name, surname, rate, days);
        // Описываем новую переменную
        this.workers = workers;
    }
    // Пишем алгоритм метода с учетом новых требований (по сути, мы просто переписываем этот метод по новому).
    getSalary() {
        return this.rate * this.days * this.workers;
     }
};

